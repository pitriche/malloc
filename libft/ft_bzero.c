#include <unistd.h>

/*
** lisibility could hugely benefit from -O3
*/

void	ft_bzero(void *s, size_t n)
{
	while (n & 0b111)
	{
		n--;
		*((char *)s + n) = 0;
	}
	while (n >= 8)
	{
		n -= 8;
		*((long *)s + (n >> 3)) = 0;
	}
}
